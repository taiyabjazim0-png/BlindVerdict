export function getHardcodedAnswer(query: string): string {
  const q = query.toLowerCase().trim();
  for (const pair of QA_BANK) {
    const matchCount = pair.keywords.filter((kw) => q.includes(kw)).length;
    if (matchCount > 0) return pair.answer;
  }
  return `Based on the case analysis, the query "${query}" requires further investigation. The available case materials have been reviewed, but additional documentation or specific context may be needed to provide a comprehensive answer.\n\nPlease try rephrasing your question or ask about:\n• Main events and incident logs\n• Available evidence\n• Legal options and defense strategy\n• Applicable IPC sections and charges\n• Bail assessment\n• Witness analysis\n• Current case status`;
}

export function FormattedAIText({ text }: { text: string }) {
  const parts = text.split(/(\*\*[^*]+\*\*)/g);
  const elements = parts.map((part, i) => {
    if (part.startsWith("**") && part.endsWith("**")) {
      return React.createElement("strong", { key: i, className: "font-semibold text-slate-800" }, part.slice(2, -2));
    }
    return React.createElement(React.Fragment, { key: i }, part);
  });
  return React.createElement("span", { className: "whitespace-pre-wrap" }, ...elements);
}
